     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data['title'] = "Laravel chart"; 
        $year = date('Y');
        //echo $year;exit;
        $users = User::where(DB::raw("(DATE_FORMAT(created_at,'%Y'))"),$year)
    				->get();
        // echo "<pre>";
        // print_r($users);exit;
        $chart = Charts::database($users, 'bar', 'highcharts')
			      ->title("Monthly new Register Users")
			      ->elementLabel("Total Users")
			      ->dimensions(1000, 500)
			      ->responsive(false)
			      ->groupByMonth($year, true);
        return view( 'chart.index' )->with( $data );
    }

